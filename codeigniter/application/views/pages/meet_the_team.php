                    <p> The development of the Online Class Record for the faculty members of the College of Engineering allows every faculty to have a unified and standardized grading system that will be based on the fixed requirements, point equivalencies, and policies set by the university.  </p>

                    <p> For the chairpersons who will be using the system, this will be a significant tool since the program will automatically send the grade sheets of the faculties under their department to their account in excel format (.xls). It also consists of calendar feature wherein they can take note of the important university, college, or department dates and deadlines.</p>

                    <p> For the faculty members, it will be an organized system that keeps records of the students including their attendance, homeworks, projects, quizzes, exams, and other requirements that need to be complied embedded with programmed solutions to automatically compute the grade equivalencies that a student achieved for a semester. Moreover, both the chairperson and faculty members are able to retrieve the students records through a summary sheet that may be kept as department's record. </p>
